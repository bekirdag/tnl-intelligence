import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { createHmac } from 'node:crypto';
import { resolve } from 'node:path';
import { format, resolveConfig } from 'prettier';

const root = resolve(import.meta.dirname, '..');
const schemaPath = resolve(root, 'schemas/webhooks/v1/envelope.schema.json');
const schema = JSON.parse(await readFile(schemaPath, 'utf8'));
const eventTypes = schema.properties.type.enum;
const version = schema.properties.schemaVersion.const;
const check = process.argv.includes('--check');

const ts = `// Generated by scripts/webhook-assets.mjs. Do not edit directly.
export const WEBHOOK_SCHEMA_VERSION = ${JSON.stringify(version)} as const;
export const WEBHOOK_EVENT_TYPES = ${JSON.stringify(eventTypes)} as const;
export type WebhookEventType = (typeof WEBHOOK_EVENT_TYPES)[number];

export interface WebhookResource {
  id: string;
  revision: number;
  url: string;
}

export interface WebhookEventData {
  summary: string;
  categories: string[];
  geographies: string[];
  entities: string[];
  assets: string[];
  impactPaths: string[];
  confidence?: number;
  language?: string;
  provenance: string[];
}

export interface WebhookEventEnvelope {
  id: string;
  type: WebhookEventType;
  schemaVersion: typeof WEBHOOK_SCHEMA_VERSION;
  occurredAt: string;
  publishedAt: string;
  tenantId: string;
  resource: WebhookResource;
  data: WebhookEventData;
  metadata: { producer: 'tnl'; traceId: string };
}
`;

const pythonValues = eventTypes.map((value) => `    ${JSON.stringify(value)},`).join('\n');
const python = `# Generated by scripts/webhook-assets.mjs. Do not edit directly.
from typing import Final, Literal

WEBHOOK_SCHEMA_VERSION: Final = ${JSON.stringify(version)}
WEBHOOK_EVENT_TYPES: Final = (
${pythonValues}
)
WebhookEventType = Literal[
${pythonValues}
]
`;

await output(resolve(root, 'packages/events/src/generated/events.ts'), ts, 'typescript');
await output(
  resolve(root, 'python/tnl_intelligence/src/tnl_intelligence/webhooks_generated.py'),
  python,
  'python',
);
