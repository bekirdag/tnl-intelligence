console.log(check ? 'Webhook generated assets match the schema' : 'Webhook assets generated');

async function output(path, content, parser) {
  const options = { ...(await resolveConfig(path)), filepath: path, parser };
  const formatted = parser === 'python' ? content : await format(content, options);
  if (check) {
    const existing = await readFile(path, 'utf8').catch(() => '');
    if (existing !== formatted) throw new Error(`${path} is stale; run npm run webhooks:generate`);
    return;
  }
  await mkdir(resolve(path, '..'), { recursive: true });
  await writeFile(path, formatted, { mode: 0o644 });
}
